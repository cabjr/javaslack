    public SelectPublicKey(Chat chat, Map<String, String> usuarioMap) {
        initComponents();
        this.chat = chat;
        this.usuarioMap = new HashMap<String, String>(usuarioMap);;
        Iterator it = this.usuarioMap.entrySet().iterator();
        jComboBox1.setModel(modelCombo);
        while (it.hasNext()) {
            Map.Entry pair = (Map.Entry) it.next();
            if (!pair.getKey().equals("slackbot")) {
                modelCombo.addElement(pair.getKey());
            }
        }
    }

